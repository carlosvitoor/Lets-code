# Faça um programa que peça as 4 notas bimestrais de um aluno
# e mostre a média aritmética delas.

n = float(input("Entre com o primeira nota:"))
n1 = float(input("Entre com o segunda nota:"))
n2 = float(input("Entre com o terceira nota:"))
n3 = float(input("Entre com o quarta nota:"))
print((n + n1 + n2 + n3)/4)
